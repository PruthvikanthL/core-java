class Bullet {
    public static void displayInfo(String type, double caliber, int weight, String material, int price) {
        System.out.println("Bullet Type: " + type);
        System.out.println("Caliber: " + caliber + " mm");
        System.out.println("Weight: " + weight + " grains");
        System.out.println("Material: " + material);
        System.out.println("Price: " + price);
    }
}